def sumar(a,b):
    """
    La función sumar requiere 2 parámetros, a y b, los cuales se suman
    La función devuelve la suma de ambos números
    """
    return a+b
def restar(a,b):
    """
    La función restar requiere 2 parámetros, a y b, los cuales se restan
    La función devuelve la resta de ambos números
    """
    return a-b
def multiplicar(a,b):
    """
    La función multiplicar requiere 2 parámetros, a y b, los cuales se mutiplican
    La función devuelve la multiplicación de ambos números
    """
    return a*b
def dividir(a,b):
    """
    La función dividir requiere 2 parámetros, a y b, los cuales se dividen
    La función devuelve la division de ambos números
    """
    return a/b
